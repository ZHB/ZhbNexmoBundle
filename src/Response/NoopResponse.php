namespace Zhb\NexmoBundle\Response;

class NoopResponse extends AbstractResponse
{
    private $message;

    public static function createFromResponse($response)
    {
        $noopResponse = new self();
        $noopResponse->setMessage('Delivery was disabled in the bundle config');

        return $noopResponse;
    }

    /**
     * @return mixed
     */
    public function getMessage()
    {
        return $this->message;
    }

    /**
     * @param mixed $message
     */
    public function setMessage($message)
    {
        $this->message = $message;
    }
}
